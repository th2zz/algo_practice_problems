class MinStack2:  # O(1) space
    def __init__(self):
        """
        initialize your data structure here.
        """
        self.stack = []  # 栈中保存差值
        self.min_value = -1

    def push(self, x: int) -> None:
        if not self.stack:
            self.stack.append(0)
            self.min_value = x
        else:
            diff = x - self.min_value
            self.stack.append(diff)
            self.min_value = self.min_value if diff > 0 else x

    def pop(self) -> None:
        if self.stack:
            diff = self.stack.pop()
            if diff < 0:
                top = self.min_value  # 返回minvalue
                self.min_value = top - diff  # 减去负数 恢复成旧的minvalue
            else:
                top = self.min_value + diff  # 返回minvalue + diff
            return top

    def top(self) -> int:
        return self.min_value if self.stack[-1] < 0 else self.stack[-1] + self.min_value

    def getMin(self) -> int:
        return self.min_value if self.stack else -1
